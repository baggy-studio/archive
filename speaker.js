function getParam(key) {
  return new URLSearchParams(window.location.search).get(key);
}

function render() {
  const id = getParam('id');
  const speaker = speakers[id];
  const main = document.getElementById('speaker-main');

  if (!speaker) {
    main.innerHTML = `
      <div style="padding:160px 48px;background:var(--blue);">
        <p style="font-family:var(--font-body);font-size:22px;color:var(--black);">Speaker not found. <a href="index.html" style="color:var(--black);text-decoration:underline;">Back to home</a></p>
      </div>`;
    return;
  }

  document.title = `${speaker.name} — Archive Conference`;

  const photoHTML = speaker.photo
    ? `<img src="${speaker.photo}" alt="${speaker.name}" class="speaker-photo" />`
    : `<div class="speaker-photo-placeholder"></div>`;

  const bioHTML = speaker.bio.map(p => `<p>${p}</p>`).join('');
  const linksHTML = speaker.links.map(l => `<a href="${l.url}" target="_blank" rel="noopener">${l.label}</a>`).join('');

  main.innerHTML = `
    <div class="speaker-hero">
      <div class="section-label">Speaker</div>
      <div class="speaker-hero-text">
        <div class="speaker-hero-name">${speaker.name}</div>
        <div class="speaker-hero-company">${speaker.company}</div>
      </div>
    </div>
    <div class="speaker-bio-section">
    ${photoHTML}
      <div class="speaker-bio-text">${bioHTML}</div>
      <div class="speaker-links-section">
        <div class="speaker-links-label">Links:</div>
        <div class="speaker-links">${linksHTML}</div>
      </div>
    </div>

  `;

  if (window.location.hash === '#speaker-main') {
    main.scrollIntoView();
  }
}

render();
